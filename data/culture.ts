export function getCulturalGroup(
  slugOrName: string,
): CulturalGroupData | undefined {
  const key = slugOrName.toLowerCase().replace(/['’]/g, "");
  return CULTURAL_GROUPS.find(
    g => g.slug === key || g.nameVi.toLowerCase().replace(/['’]/g, "") === key,
  );
}
